        protected override void Update(GameTime gameTime)
        {
            if (GamePad.GetState(PlayerIndex.One).Buttons.Back == ButtonState.Pressed || Keyboard.GetState().IsKeyDown(Keys.Escape))
                Exit();

            previousMouseState = mouseState;
            mouseState = Mouse.GetState();
            KeyboardState = Keyboard.GetState();

            this.Window.Title = mouseState.Position.ToString();

            seconds += (float)gameTime.ElapsedGameTime.TotalSeconds;

            if (screen == Screen.main)
            {
                if (mouseState.LeftButton == ButtonState.Pressed && previousMouseState.LeftButton == ButtonState.Released && playButtonRect.Contains(mouseState.Position))
                {
                    screen = Screen.veilOfDaze;
                }

                if (mouseState.LeftButton == ButtonState.Pressed && previousMouseState.LeftButton == ButtonState.Released && settingsButtonRect.Contains(mouseState.Position))
                {
                    screen = Screen.menu;
                }

                if (mouseState.LeftButton == ButtonState.Pressed && previousMouseState.LeftButton == ButtonState.Released && quitButtonRect.Contains(mouseState.Position))
                {
                    Exit();
                }
            }

            else if (screen == Screen.menu)
            {

            }

            else if (screen == Screen.chamberOfLegends)
            {

            }

            else if (screen == Screen.veilOfDaze)
            {
                Rectangle oldPosition = yukiTextureRect ;

                if (KeyboardState.IsKeyDown(Keys.W) || KeyboardState.IsKeyDown(Keys.Up))
                {
                    currentYuki = yukiForward;
                    yukiTextureRect.Y -= 2;
                }
                else if (KeyboardState.IsKeyDown(Keys.S) || KeyboardState.IsKeyDown(Keys.Down))
                {
                    currentYuki = yukiBackward;
                    yukiTextureRect.Y += 2;
                }
                else if (KeyboardState.IsKeyDown(Keys.A) || KeyboardState.IsKeyDown(Keys.Left))
                {
                    currentYuki = yukiLeft;
                    yukiTextureRect.X -= 2;
                }
                else if (KeyboardState.IsKeyDown(Keys.D) || KeyboardState.IsKeyDown(Keys.Right))
                {   
                    currentYuki = yukiRight;
                    yukiTextureRect.X += 2;
                }

                foreach (Rectangle wall in walls)
                {
                    if (yukiTextureRect.Intersects(wall))
                    {
                        yukiTextureRect = new Rectangle(oldPosition.X, oldPosition.Y, oldPosition.Width, oldPosition.Height);
                        break;
                    }
                }

                if (yukiTextureRect.Left < 0)
                {
                    yukiTextureRect.X = 0;
                }
                if (yukiTextureRect.Right > window.Width)
                {
                    yukiTextureRect.X = window.Width - yukiTextureRect.Width;
                }
                if (yukiTextureRect.Top < 0)
                {
                    yukiTextureRect.Y = 0;
                }
                if (yukiTextureRect.Bottom > window.Height)
                {
                    yukiTextureRect.Y = window.Height - yukiTextureRect.Height;
                }
            }

            else if (screen == Screen.endGame)
            {

            }


            base.Update(gameTime);
        }

        protected override void Draw(GameTime gameTime)
        {
            GraphicsDevice.Clear(Color.Black);

            _spriteBatch.Begin();

            if (screen == Screen.main)
            {
                _spriteBatch.Draw(introBg, window, Color.White);
                _spriteBatch.Draw(veilOfDazeTitle, veilOfDazeTitleRect, Color.White);
                _spriteBatch.Draw(butterfly, butterflyRect, Color.White);
                _spriteBatch.Draw(playButton, playButtonRect, Color.White);
                _spriteBatch.Draw(settingsButton, settingsButtonRect, Color.White);
                _spriteBatch.Draw(quitButton, quitButtonRect, Color.White);
            }

            else if (screen == Screen.menu)
            {
                //_spriteBatch.Draw(menuBg, window, Color.White);
                _spriteBatch.Draw(settingsTitle, settingsTitleRect, Color.White);

            }

            else if (screen == Screen.chamberOfLegends)
            {
                _spriteBatch.Draw(chamberOfLegendsBg, window, Color.White);
                _spriteBatch.Draw(chamberOfLegendsTitle, chamberOfLegendsTitleRect, Color.White);
            }

            else if (screen == Screen.veilOfDaze)
            {
                foreach (Rectangle wall in walls)
                {
                    _spriteBatch.Draw(lava, wall, Color.White);
                }
                _spriteBatch.Draw(currentYuki, yukiTextureRect, Color.White);

            }

            else if (screen == Screen.endGame)
            {

            }

            _spriteBatch.End();

            base.Draw(gameTime);
        }
